void main() {
	bool b = false;
	b = 3 < 4; // '<' less than operator
	b = 3 > 4; // '>' greater than operator
	b = 3 <= 4; // '<=' less than equal to operator
	b = 3 >= 4; // '>=' greater than equal to operator
	b = 3 != 4; // '!=' not equal to operator
	b = 3 == 4; // '==' equal to operator
}

