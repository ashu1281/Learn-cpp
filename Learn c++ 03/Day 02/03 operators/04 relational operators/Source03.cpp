void main() {
	bool b = false;
	int u = 3, v = 4;
	b = u < v; // '<' less than operator
	b = u > v; // '>' greater than operator
	b = u <= v; // '<=' less than equal to operator
	b = u >= v; // '>=' greater than equal to operator
	b = u != v; // '!=' not equal to operator
	b = u == v; // '==' equal to operator
}
