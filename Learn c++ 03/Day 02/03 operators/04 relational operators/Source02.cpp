void main() {
	bool b = false;
	int u = 3;
	b = u < 4; // '<' less than operator
	b = u > 4; // '>' greater than operator
	b = u <= 4; // '<=' less than equal to operator
	b = u >= 4; // '>=' greater than equal to operator
	b = u != 4; // '!=' not equal to operator
	b = u == 4; // '==' equal to operator
}
